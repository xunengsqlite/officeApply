package com.fnst.officeapply.exception;

@SuppressWarnings("serial")
public class OfficeException extends Exception {
	
	public OfficeException(){}
	
	public OfficeException(String msg) {
		super(msg);
	}
	public OfficeException(String msg, Throwable cause) {
		super(msg, cause);
	}
	
	public OfficeException(Throwable cause){
		super(cause);
	}
}
