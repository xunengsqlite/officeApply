package com.fnst.officeapply.entity;

//该类与数据库中HistoryRecordList数据表对应
public class HistoryRecordList {

	private int historyRecordListID;      //历史记录ID
	private String historyRecordDate;     //历史记录时间
	private String note;                  //相关备注信息
	private int groupID;
	private String groupName;
	
    public HistoryRecordList() {
	}

	// 以下是对上述信息的get与set方法
	public int getHistoryRecordListID() {
		return historyRecordListID;
	}

	public void setHistoryRecordListID(int historyRecordListID) {
		this.historyRecordListID = historyRecordListID;
	}

	public String getHistoryRecordDate() {
		return historyRecordDate;
	}

	public void setHistoryRecordDate(String historyRecordDate) {
		this.historyRecordDate = historyRecordDate;
	}

	public String getNote() {
		return note;
	}

	public void setNote(String note) {
		this.note = note;
	}

	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}

	public String getGroupName() {
		return groupName;
	}

	public void setGroupID(int groupID) {
		this.groupID = groupID;
	}

	public int getGroupID() {
		return groupID;
	}
	
}
